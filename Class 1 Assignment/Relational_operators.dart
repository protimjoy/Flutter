void main() {
  int n1 = 10, n2 = 21;

  var a = n1 > n2;
  print("n1 is greater than n2 : $a");

  var b = n1 < n2;
  print("n1 is less than n2 : $b");

  var c = n1 >= n2;
  print("n1 is greater than or equal to n2 : $c");

  var d = n1 <= n2;
  print("n1 is less than or equal to n2 : $d");

  var e = n1 == n2;
  print("n1 is equal to n2 : $e");

  var f = n1 != n2;
  print("n1 is not equal to n2 : $f");
}
