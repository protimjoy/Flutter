void main() {
  int a = 2;
  int b = 7;
//Bitwise AND
  var c = a & b;
  print(c);
//Bitwise OR
  var d = a | b;
  print(d);
//Bitwise XOR
  var e = a ^ b;
  print(e);
//Bitwise NOT
  var f = ~a;
  print(f);
//Left Shift
  var g = a << b;
  print(g);
//Right Shift
  var h = a >> b;
  print(h);
}
